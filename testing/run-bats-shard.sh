#!/usr/bin/env bash
set -euo pipefail

# run-bats-shard.sh — Shared BATS shard selector/runner for CI and local parity.
#
# Usage:
#   bash testing/run-bats-shard.sh <shard> <total-shards> [--print-files] <files...>
#
# With --print-files, prints the selected files (one per line) and exits 0.
# Without it, runs bats on the selected files and exits with the bats status.

SHARD="${1:-}"
TOTAL_SHARDS="${2:-}"
PRINT_FILES=false

usage() {
  echo "usage: run-bats-shard.sh <shard> <total-shards> [--print-files] <files...>" >&2
  exit 1
}

[ -n "$SHARD" ] || usage
[ -n "$TOTAL_SHARDS" ] || usage
shift 2

if [ "${1:-}" = "--print-files" ]; then
  PRINT_FILES=true
  shift
fi

case "$SHARD" in
  ''|*[!0-9]*) usage ;;
esac
case "$TOTAL_SHARDS" in
  ''|*[!0-9]*) usage ;;
esac

if [ "$TOTAL_SHARDS" -le 0 ]; then
  usage
fi
if [ "$SHARD" -ge "$TOTAL_SHARDS" ]; then
  usage
fi

files=("$@")

if [ "${#files[@]}" -eq 0 ]; then
  if [ "$PRINT_FILES" = true ]; then
    exit 0
  fi
  echo "Shard $SHARD: no bats files provided, skipping"
  exit 0
fi

# Weight-based greedy bin packing for balanced shard assignment.
#
# Uses measured execution times from shard-weights.txt (generated by
# measure-shard-weights.sh) as the primary weight source. Falls back to
# @test count + 1 for files not in the weights file. Assigns files
# heaviest-first to the shard with the lowest total weight.
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
WEIGHTS_FILE="$SCRIPT_DIR/shard-weights.txt"

# Load weights file into a lookup string (avoid associative arrays for bash 3 compat).
# Format: newline-separated "basename\tweight" entries.
weights_lookup=""
if [ -f "$WEIGHTS_FILE" ]; then
  while IFS=$'\t' read -r w name; do
    case "$w" in ''|'#'*) continue ;; esac
    # Multiply seconds by 100 to create a coarse ranking that
    # differentiates files with similar counts. Add 1 so 0s files
    # still distribute evenly.
    weights_lookup+="$name"$'\t'"$((w * 100 + 1))"$'\n'
  done < "$WEIGHTS_FILE"
fi

lookup_weight() {
  local fullpath="$1"
  local bname
  bname="$(basename "$fullpath")"
  local found
  found=$(printf '%s' "$weights_lookup" | grep -m1 "^${bname}"$'\t' || true)
  if [ -n "$found" ]; then
    printf '%s' "$found" | cut -f2
  else
    # Fallback: @test count + 1
    local c
    c=$(grep -c '@test' "$fullpath" 2>/dev/null || true)
    c=${c:-0}
    echo $((c + 1))
  fi
}

weighted_list=""
for f in "${files[@]}"; do
  w=$(lookup_weight "$f")
  weighted_list+="$w $f"$'\n'
done

sorted_by_weight=$(printf '%s' "$weighted_list" | sort -t' ' -k1,1rn -k2)

shard_totals=()
for ((s=0; s<TOTAL_SHARDS; s++)); do
  shard_totals[$s]=0
done

shard_files=()
while IFS=' ' read -r weight filepath; do
  [ -n "$filepath" ] || continue
  min_s=0
  min_w=${shard_totals[0]}
  for ((s=1; s<TOTAL_SHARDS; s++)); do
    if [ "${shard_totals[$s]}" -lt "$min_w" ]; then
      min_s=$s
      min_w=${shard_totals[$s]}
    fi
  done
  shard_totals[$min_s]=$((min_w + weight))
  if [ "$min_s" -eq "$SHARD" ]; then
    shard_files+=("$filepath")
  fi
done <<< "$sorted_by_weight"

if [ "$PRINT_FILES" = true ]; then
  if [ "${#shard_files[@]}" -gt 0 ]; then
    printf '%s\n' "${shard_files[@]}"
  fi
  exit 0
fi

echo "Shard $SHARD: running ${#shard_files[@]} of ${#files[@]} bats files"
if [ "${#shard_files[@]}" -eq 0 ]; then
  echo "Shard $SHARD: no bats files assigned, skipping"
  exit 0
fi

bats "${shard_files[@]}"